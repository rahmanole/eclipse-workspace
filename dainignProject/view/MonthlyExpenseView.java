    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:

        if (member == null && lastMonthSummary == null) {
            lbl_msgs.setText("Perform search first");
            lbl_msgs.setForeground(Color.red);
        } else {
            double needToPay = Double.parseDouble(lbl_needToPay.getText().trim());
            MonthlyExpense monthlyExpense = new MonthlyExpense(cardNo, lastMonthSummary.getBumping(), needToPay, new Date(System.currentTimeMillis()));
            
            
            monthlyExpensessServices.saveMonthlyExpense(monthlyExpense,manager);
            
            
            lbl_msgs.setText(cardNo + " paid monthly expense");
            lbl_msgs.setForeground(Color.red);
        }
    }//GEN-LAST:event_btn_saveActionPerformed

