    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String pName = t_productName.getText().trim();
        String pCode = t_productCode.getText().trim();
        String catname = jComboBox1.getSelectedItem().toString();
        int qty = Integer.parseInt(t_qty.getText().trim());
        double unitPrice = Double.parseDouble(t_unitPrice.getText().trim());
        double totalPrice = Double.parseDouble(t_totalPrice.getText().trim());
        Date date = new Date(System.currentTimeMillis());

        ProductCategory pc = pSCatervice.getProductCatByProductName(catname);

        Product product = new Product(pName, pCode, qty, unitPrice, totalPrice, date, pc);
        if(pService.save(product)>0){
            lbl_validation.setText("Data Saved");
            addToTable(product);
            Summary summery = sService.isProductExistsInSummaryTable(pCode);
            if(summery == null){
                sService.save(product);
            }else{
                sService.update(summery, product);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void t_unitPriceKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_t_unitPriceKeyReleased
        // TODO add your handling code here:
        try {
            int qty = Integer.parseInt(t_qty.getText().trim());
            double unitPrice = Double.parseDouble(t_unitPrice.getText().trim());

            if (qty > 0 && unitPrice > 0) {
                t_totalPrice.setText(qty * unitPrice + "");
            }
        } catch (NumberFormatException e) {
            lbl_validation.setText("Enter Number");
        }
    }//GEN-LAST:event_t_unitPriceKeyReleased

