    private void addToTable(Product product){
        DefaultTableModel model = (DefaultTableModel) tbl_purchaseDetails.getModel();
        
        Object[] obj = new Object[7];
        obj[0] = product.getProductName();
        obj[1] = product.getProductCode();
        obj[2] = product.getCat().getCatName();
        obj[3] = product.getQty();
        obj[4] = product.getUntiPrice();
        obj[5] = product.getTotalPrice();
        obj[6] = product.getDate();
        model.addRow(obj);
    }
