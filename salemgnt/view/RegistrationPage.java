    private void com_roleListItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_com_roleListItemStateChanged
        // TODO add your handling code here:
        String role_name = com_roleList.getSelectedItem().toString();
        
        if(role_name.equals("All Roles")){
            showDataInTable((ArrayList<User>) dbsrvc.getUsers());    
        }else{
            showDataInTable((ArrayList<User>) dbsrvc.getUsersByRole(role_name));
        }
        
       
    }//GEN-LAST:event_com_roleListItemStateChanged
    
