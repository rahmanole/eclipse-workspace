    private void btn_registerUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registerUserActionPerformed
        // TODO add your handling code here:
        String name = t_Name.getText().trim();
        String userName = t_username.getText().trim();
        String email = t_email.getText().trim();
        String role_name = com_roleList.getSelectedItem().toString();
        String mobile = t_mobile.getText().trim();
        String pass1 = t_pass1.getText().trim();
        String pass2 = t_pass2.getText().trim();

        UserDBServices usrService = new UserDBServices();

        Role role = usrService.getRoleByRoleName(role_name);

        User user = new User(name, userName, email, role, mobile, pass1);
        addToTable(user);

        if (usrService.isEmailExists(email)) {
            lbl_msgs.setText("Email already exists!!");
        } else {
            int isSaved = usrService.saveUser(user);

            if (isSaved > 0) {
                t_Name.setText("");
                t_username.setText("");
                t_email.setText("");
                com_roleList.setSelectedItem("");
                t_mobile.setText("");
                t_pass1.setText("");
                t_pass2.setText("");
                lbl_msgs.setText("Data saved");
                
                clear();
            } else {
                lbl_msgs.setText("Failed to save!!");
            }
        }


    }//GEN-LAST:event_btn_registerUserActionPerformed

    static User user;

    private void btn_editUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editUserActionPerformed
        // TODO add your handling code here:
        String name = t_Name.getText().trim();
        String userName = t_username.getText().trim();
        String email = t_email.getText().trim();
        String role_name = com_roleList.getSelectedItem().toString();
        String mobile = t_mobile.getText().trim();
        String pass1 = t_pass1.getText().trim();
        String pass2 = t_pass2.getText().trim();


        Role role = dbsrvc.getRoleByRoleName(role_name);

        user.setName(name);
        user.setUserName(userName);
        user.setEmail(email);
        user.setRole(dbsrvc.getRoleByRoleName(role_name));
        user.setMobile(mobile);
        user.setPass(pass1);

        int isUpdated = dbsrvc.update(user);

        if (isUpdated > 0) {
            lbl_msgs.setText("Data saved");
            showDataInTable((ArrayList<User>) dbsrvc.getUsersByRole(role_name)); 
            clear();
        } else {
            lbl_msgs.setText("Failed to save!!");
        }


    }//GEN-LAST:event_btn_editUserActionPerformed

    private void tbl_usersDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_usersDataMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tbl_usersData.getModel();
        int selectedRow = tbl_usersData.getSelectedRow();

        t_Name.setText(model.getValueAt(selectedRow, 0).toString());
        t_username.setText(model.getValueAt(selectedRow, 1).toString());
        t_email.setText(model.getValueAt(selectedRow, 2).toString());
        com_roleList.setSelectedItem(model.getValueAt(selectedRow, 3).toString());
        t_mobile.setText(model.getValueAt(selectedRow, 4).toString());

        UserDBServices usrService = new UserDBServices();
        user = usrService.getUserByEmail(model.getValueAt(selectedRow, 2).toString().trim());
    }//GEN-LAST:event_tbl_usersDataMouseClicked

